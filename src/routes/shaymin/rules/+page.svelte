<div class="{styles.rules} zain-light">
	<p class={styles.warning}>
		Please note that the contents of our rules are <b>NOT</b> all-inclusive.
		Moderation action may be taken at any time as needed to maintain a fun, chill server environment.
		Some punishments may not pertain to any rule specifically, if at all, especially during events.
	</p>
	<p class="{styles.severe} zain-bold">It is your responsibility to stay up-to-date with our rules.</p>

	<h1 class="zain-bold">How Rules are Enforced</h1>
	<p>
		We do not currently employ a standardized punishment system, such as a '3-strike rule' or similar.<br>
		However, there are some common types of punishment that will be issued when rules are broken.
	</p>

	<h2 class="zain-bold">Kick</h2>
	<p>
		Usually issued with warnings for certain rule violations.<br>
		Typically will not lead to a ban on their own.
	</p>

	<h2 class="zain-bold">Mute</h2>
	<p>
		Usually only issued for conduct issues, such as <b>emote spam</b> or <b>mild toxicity.</b><br>
		Sometimes are permanent, sometimes are temporary.
	</p>

	<h2 class="zain-bold">Temporary Ban</h2>
	<p>
		Typically 7 days, and no longer than 30 days.<br>
		More severe rule violations typically receive longer temporary bans.<br>
		Multiple temporary bans are uncommon unless extremely unrelated and over a large period of time.
	</p>
	<p class={styles.severe}>
		If you receive one of these, it is very likely your next punishment will be a <b>permanent ban.</b><br>
		This is especially true if you repeat behavior you were temporarily banned for.
	</p>

	<h2 class="zain-bold">Permanent Ban</h2>
	<p>
		A permanent punishment that prevents you from logging into the server.<br>
		This is for the most severe of rule violations, typically when players show some sort of <b>deliberate malicious intent.</b><br>
		Often, these are issued against cheaters and griefers.<br>
		However, these bans may be issued for more behavioral reasons, such as <b>toxicity or general misconduct.</b>
	</p>

	<h1 class="zain-bold">How to Request an Appeal</h1>
	<p>
		We do not currently have an official appeal system. Until we do, you are free to message a moderator on Discord or in-game.
	</p>

	<hr>

	<h1 class="zain-bold">General Behaviour Rules</h1>
	<h2 class="zain-bold">Treat everyone with respect</h2>
	<p>
		<b>Do not be rude or hostile towards other players</b>, including in your communications with them outside the server.<br>
		Toxicity and/or personal quarrels that bleed over into the server often end up a problem mods have to deal with.<br>
		It's possible to be <b>permanently banned</b> for simply causing too much trouble on the server, so keep it chill and to a minimum as much as possible.
	</p>
	<p>
		<b>Hate campaigning, spreading rumors, or otherwise petty/toxic behaviour is not allowed.</b><br>
		This is up to moderator discretion on what constitutes each of these things.<br>
		In general, baseless accusations aren't cool. Reports will not be taken at face value.<br>
		<b>If you don't like someone, do your best to avoid interacting with them on the server.</b><br>
		Please report any behaviour like this as soon as you notice it happening.
	</p>

	<h2 class="zain-bold">Don't enforce rules yourself</h2>
	<p>
		If someone's negatively impacting your experience, even if it's slightly outside of the rules, <b>report it to mods.</b><br>
		Do not retaliate, as retaliation makes it harder for us to fairly punish all parties involved.
	</p>

	<h2 class="zain-bold">No cheats or exploits</h2>
	<p>
		Anything that constitutes cheating (such as x-ray, speed/flight hacks, autoclickers/macros, Baritone, etc.) may result in a <b>permanent ban.</b><br>
		If a mod gives you more information or better abilities than intended (such as free cam), it should not be used.
	</p>
	<p>
		<b>Do not abuse duplication bugs or other exploits in the game.</b><br>
		If you find an issue that could duplicate items, give an infinite amount of resources, or otherwise seems broken, <b>please report the issue for a reward.</b>
	</p>
	<p>
		<b>Attempting to reverse the world seed may result in a permanent ban.</b><br>
		Please use the world map or ask around if looking for a particular structure or point of interest.
	</p>

	<h2 class="zain-bold">No griefing or stealing</h2>
	<p>
		All player-made builds and creations should not be touched unless you have permission.<br>
		This includes adding things onto these builds, such as digging a large moat around someone's claim.<br>
		<b>If it's not yours, don't mess with it.</b><br>
		Generally, these issues shouldn't be too prevalent due to our claim system.
	</p>

	<h2 class="zain-bold">Keep chat clean</h2>
	<p>
		Due to the difficulty of moderating multiple languages, <b>chat may only be in English.</b><br>
		There are exceptions to this, such as commonly known words and phrases, but generally players should steer clear of this.<br>
		Use of specific slurs, racist remarks, or extreme obscenity will result in a <b>permanent ban.</b><br>
		Emote spam or mild toxicity may result in a <b>kick</b> or <b>mute.</b>
	</p>

	<h2 class="zain-bold">No lag machines</h2>
	<p>
		Building lag machines or abusing bugs in the game to otherwise degrade the experience of other players is not allowed, and <b>may result in a ban.</b><br>
		Particular server-intensive builds or farms may be disabled/nerfed if they're causing immense lag or making it difficult for others to play.
	</p>

	<h2 class="zain-bold">Don't abuse the claim system</h2>
	<p>
		Do not claim limited structures and restrict access to them, such as strongholds or ancient cities.<br>
		<b>These areas are public resources</b> and should be kept available for all players to use and benefit from.<br>
		You're welcome to raid them for resources, but don't prevent others from entering.<br>
		Keep in mind - claims, by default, prevent players from modifying blocks in that area.
	</p>
	<p>
		<b>Don't claim natural resources just to claim them.</b><br>
		If you're building a base at a dungeon or trial chamber - cool. If not, <b>don't claim it.</b><br>
		Claims preventing access to these things with no obvious player build, or an insignificant amount of progress after request, <b>will be removed.</b>
	</p>
	<p>
		Don't claim large portions of land around a town or base. Don't claim other people's builds, either. <b>Leave other players private bases alone.</b>
	</p>
	<p>
		<b>Claiming land at spawn to sell, loan, give out, or otherwise offer to other players is not allowed.</b><br>
		Only claim what you plan to use for your own builds.<br>
		Similarly, <b>claiming land solely to sell it to other players for in-game currency or items is not allowed</b>.
	</p>
	<p>
		<b>Don't force other players to have their base claimed by you.</b> Let players claim their own builds.
	</p>
	<p>
		<b>Don't claim a large amount of land nearby spawn.</b> There is limited real estate near spawn for player builds.<br>
		Please be considerate of other players and limit the amount of land you or your group has claimed to mirror that of other players.<br>
		Claiming large amounts of land for a potential town should not be done within 800 blocks (50 chunks) of spawn.
	</p>

	<h2 class="zain-bold">Don't be obnoxiously loud</h2>
	<p>
		Music machines are fine, though it's likely they'll stop functioning properly if server performamnce degrades.<br>
		Anything else that's intended to be loud should not be built and <b>will be removed/disabled by mods.</b><br>
		Abuse of goat horns is also not allowed.
	</p>

	<h2 class="zain-bold">No inappropriate paintings</h2>
	<p>
		In general, all paintings should be appropriate, noting that the server is a mature environment.<br>
		Below are some examples of what is and isn't allowed, though please keep in mind that this list is <b>not all-inclusive,</b> and may be updated as more ambiguous situations are found.
	</p>

	<h3>Examples of inappropriate paintings</h3>
	<p>
		Nudity, exposed genitalia, or any sexually explicit scenarios<br>
		Sexually framed images, such as subjects in sexually evocative poses or dressed in overly evocative or exposing attire<br>
		Paintings with an exclusive focus on sexual aspects of a subject, such as the character's butt, crotch, or chest<br>
		Any paintings that appear to depict sexual fluids, or implied as such<br>
		Paintings that were solely created to graphically depict a fetish with no other clear purpose<br>
		Extreme depictions of gore<br>
		Depictions of self-harm, suicide, or real world deaths, or imagery that heavily implies such
	</p>

	<h3>Examples of appropriate paintings</h3>
	<p>
		Anything you might find in a movie outside of any excessively gory or sexual imagery, or any otherwise sexually evocative scenes<br>
		Non-sexualized depiction of subject(s) in swimsuits or other somewhat exposing, everyday attire such as 'crop tops'<br>
		Depictions of blood in humorous or horror contexts, for tasteful artistic purposes, or as a detail element in builds
	</p>

	<h2 class="zain-bold">No exploiting loopholes</h2>
	<p>
		If it feels like a loophole, <b>it probably is.</b>
	</p>
